using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.UI;
using System.Web.UI.WebControls;

namespace ASPNETSimpleForms
{
    public partial class SiteMaster : System.Web.UI.MasterPage
    {
        protected void Page_Load(object sender, EventArgs e)
        {
            if (Page.User.Identity.IsAuthenticated)
            {
                requestAuthenticated.Text = string.Format("You are Authenticated. Welcome {0}", Page.User.Identity.Name);
            }
            else
            {
                requestAuthenticated.Text = "You are not Authenticated";
            }
        }
    }
}
